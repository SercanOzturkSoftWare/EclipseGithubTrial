package javaCalismalari;

public class cokluOperatorler {

	public static void main(String[] args) {
		// += operatoru   esitlikte sagdaki degeri soldakine ekle ve cikan sonucu soldakine ata demektir 	
				int a = 12;
				a += 5;
				System.out.println(a);

				// -= operatoru esitlikte soldaki degerden sagdaki degeri cikar ve sonucu
				// soldaki degiskene ata demektir
				int b = 15;
				b -= 3;
				System.out.println(b);

		// *= operatoru   esitlikte sagdaki deger ile soldakini carp ve sonucu soldaki degiskene ata demektir 		
				int c = 13;
				c *= 3;
				System.out.println(c);

		// /= operatoru esitlikteki soldaki degeri sagdakine boler ve cikan sonucu soldaki degiskene atar 		

				int d = 18;
				d /= 6;
				System.out.println("sonuc: " + d);

		// %= operatoru esitlikteki soldaki degeri sagdakine boler ve kalan sonucu soldaki degiskene atar		
				int e = 24;
				e %= 9;
				System.out.println("sonuc: " + e);

		
	}

}
